public class AssignmentOp {
    
    public static void main(String args[]){
        int a=10;
        int b=20;
        a+=4; //a=a+4
        b-=4; //b=b-4

        System.out.println(a);
        System.out.println(b);

        int z=10;

        z+=3; //z=z+3
        System.out.println(z); //13

        z-=4; //z=z-4
        System.out.println(z); //9

        z*=2; //z=z*2
        System.out.println(z); //18

        z/=2; //z=z/2
        System.out.println(z); //9
    }
}
